export const hasRoutePermission = (
  route: string,
  userPermissions: Permission[]
) => {
  const rp = routePermissions[route];
  if (rp === undefined) return true;
  const perm = Array.isArray(rp) ? rp : [rp];
  return perm.some((p) => userPermissions.includes(p));
};
